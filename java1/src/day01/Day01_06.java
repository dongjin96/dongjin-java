package day01;

import java.util.Scanner;

public class Day01_06 {// c s
public static void main(String[] args) {
	Scanner 입력객체 = new Scanner(System.in);
	 // 2. 입력받기
	System.out.println("작성자 : "); String writer = 입력객체.next();
	System.out.println("내용 : "); String contents = 입력객체.nextLine();//공백 포함 o
		// scanner.nextLine(); // next 다음으로  nextLine 사용시 문제 발생
		// 해결방법  : next 와 nextLine 사이에 nextLine() 추가
	System.out.println("날짜"); String date = 입력객체.next();
	
	// 3. 출력
	System.out.println("--------------방문록-------------");
	System.out.println("|순번 | 작성자 | 내용 \t 날짜 |");
	System.out.println("| 1  | "+writer+"|"+contents + "\t | " + date + "|");
	System.out.println("----------------------------------");

}
}// c e
